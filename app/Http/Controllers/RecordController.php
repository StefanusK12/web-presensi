    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id, Request $request)
    {
        $tglawal = $request->tglawal;
        $tglakhir = $request->tglakhir;

        if($tglakhir < $tglawal){
            return redirect('/dashboard/records')->with('fail', 'Date invalid!');
        }

        $presensi = Presensi::with('user')->where('user_id', auth()->user()->id)->whereBetween('tanggal',[$tglawal, $tglakhir])->orderBy('tanggal','asc')->get();

        return view('dashboard.record.report', compact('presensi'));
    }

